package com.asharia;

public class Coordinates {

	private int x1, y1, x2, y2;

	public int getX1() {
		return x1;
	}

	public Coordinates setX1(int x1) {
		this.x1 = x1;
		return this;
	}

	public int getY1() {
		return y1;
	}

	public Coordinates setY1(int y1) {
		this.y1 = y1;
		return this;
	}

	public int getX2() {
		return x2;
	}

	public Coordinates setX2(int x2) {
		this.x2 = x2;
		return this;
	}

	public int getY2() {
		return y2;
	}

	public Coordinates setY2(int y2) {
		this.y2 = y2;
		return this;
	}
}